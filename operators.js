//additional
const sum = (a, b) => "Added values is " + ":" + (a + b);
console.log(sum(20, 30));

//subtract
const minus = (a, b) => "subtracted values is " + ":" + (a - b);
console.log(minus(10, 3));

// multiplication
const mul = (a, b) => "multiplied values is " + ":" + a * b;
console.log(mul(9, 3));

// div
const div = (a, b) => "divided values is " + ":" + a / b;
console.log(div(64, 8));

// modulus
const mod = (a, b) => "modulus values is " + ":" + (a % b);
console.log(mod(10, 4));

// ++
const increment = (a) => "Increased values is " + ":" + a++;
console.log(increment(2));

// --
const decrement = (a) => "Decreased values is " + ":" + --a;
console.log(decrement(3));
